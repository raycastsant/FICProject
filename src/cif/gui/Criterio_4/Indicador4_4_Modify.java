    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
   try {
                    int existe = 0;
                    String existen = "";
                    ArrayList<C_4_4_Object> listaAct = new ArrayList<C_4_4_Object>();
                    for(int i=0; i<modelSeleccionados.size(); i++)
                    {
                     C_4_4_Object object = (C_4_4_Object)modelSeleccionados.get(i);

                     if(anno != princ.getSelectedYear())
                      existe = DBAccess.verificarPkey2("select id,anno,municipio ", tablename, " where id='"+id+"' and municipio='"+mun+"' " +
                                                      "and anno="+princ.getSelectedYear()+" and id_pfnm="+object.getProducto().Id());
                     if(existe != 1)
                      listaAct.add(object);
                     else
                      existen += object.getProducto().Nombre()+",";

                     existe = 0;
                    }

                    if(existen.length() > 0)
                    {
                     existen = existen.substring(0, existen.length()-1);
                     String mens = "Los siguientes productos ya existen en el sistema para el periodo "+princ.getSelectedYear()+": "+"\n"+existen+"\n";
                     mens += "Los datos de estos no se actualizarÃ¡n!!!";
                     JOptionPane.showMessageDialog(null, mens);
                    }

                    if(listaAct.size() > 0)
                     ActualizarPFNM(listaAct);

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al actualizar elementos");
        }
    }//GEN-LAST:event_jBAceptarActionPerformed

    private void addProductToList(C_4_4_Object prod)
    {
     modelSeleccionados.addElement(prod);
    }

    private void restartFieldValues()
    {
     jTFCantidad.setText("0");
     jTFPotencial.setText("0");
     jTFValor.setText("0.0");
    }

    private void SetFielValuesEnabled(boolean flag)
    {
     restartFieldValues();
     jLCantidad.setEnabled(flag);
     jLPotencial.setEnabled(flag);
     jLValor.setEnabled(flag);
     jTFCantidad.setEnabled(flag);
     jTFPotencial.setEnabled(flag);
     jTFValor.setEnabled(flag);
     jBAsignar.setEnabled(flag);
    }

    private void SetFieldValues(C_4_4_Object object)
    {
     jTFCantidad.setText(object.getCantidad().toString());
     jTFPotencial.setText(object.getPotencial().toString());
     jTFValor.setText(object.getValor().toString());
    }

    private void ActualizarPFNM(ArrayList<C_4_4_Object> listaAct) throws SQLException
    {
     for(int i=0; i<listaAct.size(); i++)
     {
      C_4_4_Object prod = (C_4_4_Object)listaAct.get(i);
      DBAccess.updateProdForNoMad(id, princ.getSelectedYear(), anno, prod.getProducto().Id(), prod.getCantidad(), prod.getPotencial(), prod.getValor(), tablename);
     }

     SetFielValuesEnabled(false);
     JOptionPane.showMessageDialog(null, "Elementos actualizados correctamente");
    }

