    private void jBAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAsignarActionPerformed
        // TODO add your handling code here:
        int index = jLSeleccionados.getSelectedIndex();
         C_4_4_Object object = (C_4_4_Object)jLSeleccionados.getSelectedValue();
         String cantValue = jTFCantidad.getText();
         String potencValue = jTFPotencial.getText();
         String valorValue = jTFValor.getText();
         int cant = 0;
         int potenc = 0;
         double valor = 0;

         if(!cantValue.trim().equals(""))
          cant = Integer.parseInt(cantValue);

         if(!potencValue.trim().equals(""))
          potenc = Integer.parseInt(potencValue);

         if(!valorValue.trim().equals(""))
          valor = Double.parseDouble(valorValue);

         object.setCantidad(cant);
         object.setPotencial(potenc);
         object.setValor(valor);

         modelSeleccionados.removeElement(object);
         modelSeleccionados.add(index, object);

         SetFielValuesEnabled(false);
}//GEN-LAST:event_jBAsignarActionPerformed

