    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:   
    try {
                    int existe = 0;
                    String talasexisten = "";
                    ArrayList<C_4_3_Object> listaAct = new ArrayList<C_4_3_Object>();
                    for(int i=0; i<modelSelectedTalas.size(); i++)
                    {
                     C_4_3_Object tala = (C_4_3_Object)modelSelectedTalas.get(i);

                     if(anno != princ.getSelectedYear())
                      existe = DBAccess.verificarPkey2("select id,anno,municipio ", tablename, " where id='"+id+"' and municipio='"+mun+"' " +
                                                      "and anno="+princ.getSelectedYear()+" and id_talas="+tala.getTala().Id());
                     if(existe != 1)
                      listaAct.add(tala);
                     else
                      talasexisten += tala.getTala().Nombre()+",";

                     existe = 0;
                    }

                    if(talasexisten.length() > 0)
                    {
                     talasexisten = talasexisten.substring(0, talasexisten.length()-1);
                     String mens = "Las siguientes talas ya existen en el sistema para el periodo "+princ.getSelectedYear()+": "+"\n"+talasexisten+"\n";
                     mens += "Los datos de estas no se actualizarán!!!";
                     JOptionPane.showMessageDialog(null, mens);
                    }

                    if(listaAct.size() > 0)
                     ActualizarTalas(listaAct);
            
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al actualizar elementos");
        }
    }//GEN-LAST:event_jBAceptarActionPerformed

    private void restartFieldValues()
    {
     jTFSupTotal.setText("0.0");
     jTFSuptecn.setText("0.0");
    }

    private void SetFielValuesEnabled(boolean flag)
    {
     restartFieldValues();
     jLSupTot.setEnabled(flag);
     jLSuptecn.setEnabled(flag);
     jTFSupTotal.setEnabled(flag);
     jTFSuptecn.setEnabled(flag);
     jBAsignar.setEnabled(flag);
    }

    private void ActualizarTalas(ArrayList<C_4_3_Object> listaAct) throws SQLException
    {
     for(int i=0; i<listaAct.size(); i++)
     {
      C_4_3_Object tala = (C_4_3_Object)listaAct.get(i);
      DBAccess.updateTalas(id, princ.getSelectedYear(), anno, tala.getTala().Id(), tala.getSupTotal(), tala.getSupTecAdec(), tablename);
     }

     SetFielValuesEnabled(false);
     JOptionPane.showMessageDialog(null, "Elementos actualizados correctamente");
    }

    private void addTalaToList(C_4_3_Object tala)
    {
     modelSelectedTalas.addElement(tala);
    }

    private void SetFieldValues(C_4_3_Object object)
    {
     jTFSupTotal.setText(object.getSupTotal().toString());
     jTFSuptecn.setText(object.getSupTecAdec().toString());
    }

