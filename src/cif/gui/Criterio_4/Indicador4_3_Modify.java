    private void jBAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAsignarActionPerformed
        // TODO add your handling code here:
        //DBAccess.insertTalas(id,anno,idtalas,supfftot, supftecninad,tablename);
         int index = jLSelectedTalas.getSelectedIndex();
         C_4_3_Object object = (C_4_3_Object)jLSelectedTalas.getSelectedValue();
         String supTotalValue = jTFSupTotal.getText();
         String supTecnValue = jTFSuptecn.getText();
         double supTot = 0;
         double supTec = 0;

         if(!supTotalValue.trim().equals(""))
          supTot = Double.parseDouble(supTotalValue);

         if(!supTecnValue.trim().equals(""))
          supTec = Double.parseDouble(supTecnValue);

         if(supTec > supTot)
         {
             String mens = "La Superficie Total no debe ser menor que la Superficie con Técnicas Adecuadas!!!";
             JOptionPane.showMessageDialog(princ, mens, "Error", JOptionPane.ERROR_MESSAGE);
         }
         else
         {
             object.setSupTotal(supTot);
             object.setSupTecAdec(supTec);

             modelSelectedTalas.removeElement(object);
             modelSelectedTalas.add(index, object);

             SetFielValuesEnabled(false);
         }
    }//GEN-LAST:event_jBAsignarActionPerformed

