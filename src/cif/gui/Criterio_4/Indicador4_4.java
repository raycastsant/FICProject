    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
                int existe;
        try {
                if (tag.equals("insert"))
                {
                    String existen = "";
                    ArrayList<C_4_4_Object> listaInsertar = new ArrayList<C_4_4_Object>();
                    for(int i=0; i<modelSeleccionados.size(); i++)
                    {
                     C_4_4_Object producto = (C_4_4_Object)modelSeleccionados.get(i);
                     existe = DBAccess.verificarPkey2("select id,anno,municipio ", tablename, " where id='"+id+"' and municipio='"+mun+"' " +
                                                      "and anno="+anno+" and id_pfnm="+producto.getProducto().Id());
                     if(existe != 1)
                      listaInsertar.add(producto);
                     else
                      existen += producto.getProducto().Nombre()+",";
                    }

                    if(existen.length() > 0)
                    {
                     existen = existen.substring(0, existen.length()-1);
                     String mens = "Los siguientes productos ya existen en el sistema: "+"\n"+existen+"\n";
                     mens += "¿Desea realizar la operación sin tenerlos en cuenta?";
                     Object[] options = {"Si", "No", "Cancelar"};
                     int res = JOptionPane.showOptionDialog(princ, mens, "Adevertencia!!!", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE,
                               null, options, options[0]);

                     if(res == JOptionPane.OK_OPTION)
                      InsertarPRODUCTOSFNM(listaInsertar);
                    }
                    else
                     InsertarPRODUCTOSFNM(listaInsertar);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Indicador4_4.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jBAceptarActionPerformed

    private void addSelectedProductToList(int index)
    {
         TipoProducto_Object prod = (TipoProducto_Object)modelTipoproductos.get(index);

         C_4_4_Object object = new C_4_4_Object("-1", prod, 0, 0, 0);
         modelSeleccionados.addElement(object);
         modelTipoproductos.removeElement(prod);

         restartFieldValues();
         jBQuitAll.setEnabled(true);

         if(modelTipoproductos.getSize() < 2)
          jBAddAll.setEnabled(false);

         jBAdd.setEnabled(false);
    }

    private void addProductToList(TipoProducto_Object prod)
    {
     modelTipoproductos.addElement(prod);
    }

    private void quitSelectedProdFromList(int index)
    {
         C_4_4_Object object = (C_4_4_Object)modelSeleccionados.get(index);

         modelSeleccionados.removeElement(object);
         modelTipoproductos.addElement(object.getProducto());

         restartFieldValues();
         jBAddAll.setEnabled(true);

         if(modelSeleccionados.getSize() < 2)
          jBQuitAll.setEnabled(false);

         jBQuit.setEnabled(false);
    }

    private void restartFieldValues()
    {
     jTFCantidad.setText("0");
     jTFPotencial.setText("0");
     jTFValor.setText("0.0");
    }

    private void SetFielValuesEnabled(boolean flag)
    {
     restartFieldValues();
     jLCantidad.setEnabled(flag);
     jLPotencial.setEnabled(flag);
     jLValor.setEnabled(flag);
     jTFCantidad.setEnabled(flag);
     jTFPotencial.setEnabled(flag);
     jTFValor.setEnabled(flag);
     jBAsignar.setEnabled(flag);
    }

    private void quitSelectedProductFromList(int index)
    {
         C_4_4_Object object = (C_4_4_Object)modelSeleccionados.get(index);

         modelSeleccionados.removeElement(object);
         modelTipoproductos.addElement(object.getProducto());

         restartFieldValues();
         jBAddAll.setEnabled(true);

         if(modelSeleccionados.getSize() < 2)
          jBQuitAll.setEnabled(false);

         jBQuit.setEnabled(false);
    }

    private void SetFieldValues(C_4_4_Object object)
    {
     jTFCantidad.setText(object.getCantidad().toString());
     jTFPotencial.setText(object.getPotencial().toString());
     jTFValor.setText(object.getValor().toString());
    }

    private void InsertarPRODUCTOSFNM(ArrayList<C_4_4_Object> listaInsertar) throws SQLException
    {
     for(int i=0; i<listaInsertar.size(); i++)
     {
      C_4_4_Object prod = listaInsertar.get(i);
      DBAccess.insertProdForNoMad(id, princ.getSelectedYear(), prod.getProducto().Id(), prod.getCantidad(), prod.getPotencial(), prod.getValor(), tablename);
     }

     while(modelSeleccionados.getSize() > 0)
     {
      C_4_4_Object object = (C_4_4_Object)modelSeleccionados.get(0);
      modelSeleccionados.removeElement(object);
     }

     SetFielValuesEnabled(false);
     JOptionPane.showMessageDialog(null, "Elementos insertados correctamente");
    }

