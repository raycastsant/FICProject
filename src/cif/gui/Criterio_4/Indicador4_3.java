    private void quitSelectedTalaFromList(int index)
    {
         C_4_3_Object object = (C_4_3_Object)modelSelectedTalas.get(index);

         modelSelectedTalas.removeElement(object);
         modelTalas.addElement(object.getTala());

         restartFieldValues();
         jBAddAll.setEnabled(true);

         if(modelSelectedTalas.getSize() < 2)
          jBQuitAll.setEnabled(false);

         jBQuit.setEnabled(false);
    }

    private void restartFieldValues()
    {
     jTFSupTotal.setText("0.0");
     jTFSuptecn.setText("0.0");
    }

    private void SetFieldValues(C_4_3_Object object)
    {   
     jTFSupTotal.setText(object.getSupTotal().toString());
     jTFSuptecn.setText(object.getSupTecAdec().toString());
    }

    private void SetFielValuesEnabled(boolean flag)
    {
     restartFieldValues();
     jLSupTot.setEnabled(flag);
     jLSuptecn.setEnabled(flag);
     jTFSupTotal.setEnabled(flag);
     jTFSuptecn.setEnabled(flag);
     jBAsignar.setEnabled(flag);
    }

    private void InsertarTALAS(ArrayList<C_4_3_Object> listaInsertar) throws SQLException
    {
     for(int i=0; i<listaInsertar.size(); i++)
     {
      C_4_3_Object tala = listaInsertar.get(i);
      DBAccess.insertTalas(id, princ.getSelectedYear(), tala.getTala().Id(), tala.getSupTotal(), tala.getSupTecAdec(), tablename);
     }

     while(modelSelectedTalas.getSize() > 0)
     {
      C_4_3_Object object = (C_4_3_Object)modelSelectedTalas.get(0);
      modelSelectedTalas.removeElement(object);
     }
     
     SetFielValuesEnabled(false);
     JOptionPane.showMessageDialog(null, "Elementos insertados correctamente");
    }

