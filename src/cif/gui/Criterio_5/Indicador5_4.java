    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
        int existe;
        try {
                if (tag.equals("insert"))
                {
                    String existen = "";
                    ArrayList<C_5_4_Object> listaInsertar = new ArrayList<C_5_4_Object>();
                    for(int i=0; i<modelSeleccionados.size(); i++)
                    {
                     C_5_4_Object serv = (C_5_4_Object)modelSeleccionados.get(i);
                     existe = DBAccess.verificarPkey2("select id,anno,municipio ", tablename, " where id='"+id+"' and municipio='"+mun+"' " +
                                                      "and anno="+anno+" and id_serv="+serv.getTipoServicio().Id());
                     if(existe != 1)
                      listaInsertar.add(serv);
                     else
                      existen += serv.getTipoServicio().Nombre()+",";
                    }

                    if(existen.length() > 0)
                    {
                     existen = existen.substring(0, existen.length()-1);
                     String mens = "Los siguientes servicios ya existen en el sistema: "+"\n"+existen+"\n";
                     mens += "¿Desea realizar la operación sin tenerlos en cuenta?";
                     Object[] options = {"Si", "No", "Cancelar"};
                     int res = JOptionPane.showOptionDialog(princ, mens, "Adevertencia!!!", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE,
                               null, options, options[0]);

                     if(res == JOptionPane.OK_OPTION)
                      InsertarSERVICIO(listaInsertar);
                    }
                    else
                     InsertarSERVICIO(listaInsertar);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Indicador5_4.class.getName()).log(Level.SEVERE, null, ex);
            }
}//GEN-LAST:event_jBAceptarActionPerformed

