    private void addSelectedServToList(int index)
    {
         TipoServicio_Object serv = (TipoServicio_Object)modelServicios.get(index);

         C_5_4_Object object = new C_5_4_Object("-1", 0, 0, serv);
         modelSeleccionados.addElement(object);
         modelServicios.removeElement(serv);

         restartFieldValues();
         jBQuitAll.setEnabled(true);

         if(modelServicios.getSize() < 2)
          jBAddAll.setEnabled(false);

         jBAdd.setEnabled(false);
    }

    private void restartFieldValues()
    {
     jTFCant.setText("0");
     jTFPlanManej.setText("0");
    }

    private void quitSelectedServFromList(int index)
    {
         C_5_4_Object object = (C_5_4_Object)modelSeleccionados.get(index);

         modelSeleccionados.removeElement(object);
         modelServicios.addElement(object.getTipoServicio());

         restartFieldValues();
         jBAddAll.setEnabled(true);

         if(modelSeleccionados.getSize() < 2)
          jBQuitAll.setEnabled(false);

         jBQuit.setEnabled(false);
    }

    private void SetFielValuesEnabled(boolean flag)
    {
     restartFieldValues();
     jLCant.setEnabled(flag);
     jLPlanManej.setEnabled(flag);
     jTFCant.setEnabled(flag);
     jTFPlanManej.setEnabled(flag);
     jBAsignar.setEnabled(flag);
    }   

    private void addServicioToList(TipoServicio_Object serv)
    {
     modelServicios.addElement(serv);
    }

    private void SetFieldValues(C_5_4_Object object)
    {
     jTFCant.setText(object.getCantidad().toString());
     jTFPlanManej.setText(object.getPlan_manejo().toString());
    }

    private void InsertarSERVICIO(ArrayList<C_5_4_Object> listaInsertar) throws SQLException
    {
     for(int i=0; i<listaInsertar.size(); i++)
     {
      C_5_4_Object serv = listaInsertar.get(i);
      DBAccess.insertServicios(id, princ.getSelectedYear(), serv.getTipoServicio().Id(), serv.getCantidad(), serv.getPlan_manejo(), tablename);
     }

     while(modelSeleccionados.getSize() > 0)
     {
      C_5_4_Object object = (C_5_4_Object)modelSeleccionados.get(0);
      modelSeleccionados.removeElement(object);
     }

     SetFielValuesEnabled(false);
     JOptionPane.showMessageDialog(null, "Elementos insertados correctamente");
    }
        
