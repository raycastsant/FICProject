    private void restartFieldValues()
    {
     jTFCant.setText("0");
     jTFPlanManej.setText("0");
    }

    private void SetFielValuesEnabled(boolean flag)
    {
     restartFieldValues();
     jLCant.setEnabled(flag);
     jLPlanManej.setEnabled(flag);
     jTFCant.setEnabled(flag);
     jTFPlanManej.setEnabled(flag);
     jBAsignar.setEnabled(flag);
    }   

    private void addServicioToList(C_5_4_Object serv)
    {
     modelSeleccionados.addElement(serv);
    }

    private void SetFieldValues(C_5_4_Object object)
    {
     jTFCant.setText(object.getCantidad().toString());
     jTFPlanManej.setText(object.getPlan_manejo().toString());
    }

    private void ActualizarServicios(ArrayList<C_5_4_Object> listaAct) throws SQLException
    {
     for(int i=0; i<listaAct.size(); i++)
     {
      C_5_4_Object serv = (C_5_4_Object)listaAct.get(i);
      DBAccess.updateServicios(id, princ.getSelectedYear(), anno, serv.getTipoServicio().Id(), serv.getCantidad(), serv.getPlan_manejo(), tablename);
     }

     SetFielValuesEnabled(false);
     JOptionPane.showMessageDialog(null, "Elementos actualizados correctamente");
    }
