    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
      try {
                    int existe = 0;
                    String existen = "";
                    ArrayList<C_5_4_Object> listaAct = new ArrayList<C_5_4_Object>();
                    for(int i=0; i<modelSeleccionados.size(); i++)
                    {
                     C_5_4_Object serv = (C_5_4_Object)modelSeleccionados.get(i);

                     if(anno != princ.getSelectedYear())
                      existe = DBAccess.verificarPkey2("select id,anno,municipio ", tablename, " where id='"+id+"' and municipio='"+mun+"' " +
                                                      "and anno="+princ.getSelectedYear()+" and id_serv="+serv.getTipoServicio().Id());
                     if(existe != 1)
                      listaAct.add(serv);
                     else
                      existen += serv.getTipoServicio().Nombre()+",";

                     existe = 0;
                    }

                    if(existen.length() > 0)
                    {
                     existen = existen.substring(0, existen.length()-1);
                     String mens = "Los siguientes servicios ya existen en el sistema para el periodo "+princ.getSelectedYear()+": "+"\n"+existen+"\n";
                     mens += "Los datos de estos no se actualizarán!!!";
                     JOptionPane.showMessageDialog(null, mens);
                    }

                    if(listaAct.size() > 0)
                     ActualizarServicios(listaAct);

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al actualizar elementos");
        }
}//GEN-LAST:event_jBAceptarActionPerformed

