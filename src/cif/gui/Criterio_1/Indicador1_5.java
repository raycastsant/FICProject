    private void jButton_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AceptarActionPerformed
        // TODO add your handling code here:
        Double area_tal_def=java.lang.Double.parseDouble(jTextField_AT1.getText());
        Double area_quem_def=java.lang.Double.parseDouble(jTextField_AQ.getText());
        Double area_tal=java.lang.Double.parseDouble(jTextField_AT2.getText());
        Double area_establ=java.lang.Double.parseDouble(jTextField_AE.getText());
        int existe;
        try{
                if(tag.equals("insert"))
                {
                    existe=DBAccess.verificarPkey(id, princ.getSelectedYear(), tablename);
                    if(existe!=1)
                    {
                        DBAccess.insertPendref(id,princ.getSelectedYear(),area_tal_def,area_quem_def,area_tal,area_establ,tablename);
                        jTextField_AT1.setText("0.0");
                        jTextField_AQ.setText("0.0");
                        jTextField_AT2.setText("0.0");
                        jTextField_AE.setText("0.0");
                    }
                else
                {
                    JOptionPane.showConfirmDialog(this, "Ya existen datos para esta entidad en el año "+princ.getSelectedYear()+".", "Error!!!", JOptionPane.CLOSED_OPTION,JOptionPane.ERROR_MESSAGE);
                }
                    }

                else if(tag.equals("modify"))
                {
                    if(anno==princ.getSelectedYear())
                    {
                        DBAccess.updatePendref(id,princ.getSelectedYear(),anno,area_tal_def,area_quem_def,area_tal,area_establ,tablename);
                        JOptionPane.showConfirmDialog(this, "Los datos han sido guardados satisfactoriamente.", "Confirmación!!!", JOptionPane.CLOSED_OPTION,JOptionPane.INFORMATION_MESSAGE);
                        anno=princ.getSelectedYear();
                    }
                    else
                    {
                        existe=DBAccess.verificarPkey(id, princ.getSelectedYear(), tablename);
                        if(existe!=1)
                        {
                            DBAccess.updatePendref(id,princ.getSelectedYear(),anno,area_tal_def,area_quem_def,area_tal,area_establ,tablename);
                            JOptionPane.showConfirmDialog(this, "Los datos han sido guardados satisfactoriamente.", "Confirmación!!!", JOptionPane.CLOSED_OPTION,JOptionPane.INFORMATION_MESSAGE);
                            anno=princ.getSelectedYear();
                        }
                        else
                        {
                            JOptionPane.showConfirmDialog(this, "Ya existen datos para esta entidad en el año "+princ.getSelectedYear()+".", "Error!!!", JOptionPane.CLOSED_OPTION,JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } catch (SQLException ex) {
            Logger.getLogger(Indicador1_5.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_AceptarActionPerformed

