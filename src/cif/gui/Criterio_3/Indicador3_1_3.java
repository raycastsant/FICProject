    private void createProgama()
    {
        try {
            String where="where active=true";
            idprog = DBAccess.selectNomenclator("c3_1_3_ejecprogprotec",where);
            objPP=new ProgramaProtec[idprog.length];
            for (int i = 0; i < idprog.length; i++) {
            objPP[i]=new ProgramaProtec(idprog[i][0].toString(), idprog[i][1].toString(),0.0);
            System.out.println(objPP[i].getNombre());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Indicador3_1_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ShowProgama()
    {
        DefaultListModel modeloPP = new DefaultListModel();
        for (int j = 0; j < objPP.length; j++) {
                modeloPP.addElement(objPP[j]);
            }
         jList_Prog.setModel(modeloPP);
    }

     private void Derecha(int index)
    {
        DefaultListModel dmPP=(DefaultListModel) jList_Prog.getModel();
        ProgramaProtec elemPP=(ProgramaProtec) dmPP.get(index);
        adatos.add(elemPP);
        ((DefaultListModel)jList_Datos.getModel()).addElement(elemPP.getNombre()+" --- Prog de Protec: 0.0");
        dmPP.remove(index);
        jList_Datos.setSelectedIndex(0);
        if (jList_Prog.getModel().getSize()>0) {
           jList_Prog.setSelectedIndex(0);
        }
    }

    private void Izquierda(int index)
    {
        DefaultListModel dfmEE=(DefaultListModel) jList_Prog.getModel();
        dfmEE.addElement(adatos.get(index));
        adatos.remove(index);
        ((DefaultListModel) jList_Datos.getModel()).remove(index);
        if (jList_Datos.getModel().getSize()>0) {
           jList_Datos.setSelectedIndex(0);
        }
        jList_Prog.setSelectedIndex(0);
    }

