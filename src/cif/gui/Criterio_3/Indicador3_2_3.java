    private void jButton_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AceptarActionPerformed
        // TODO add your handling code here:
        Double longprinc=java.lang.Double.parseDouble(jTextField_Lprinc.getText());
        Double arfprinc=java.lang.Double.parseDouble(jTextField_ARFprinc.getText());
        Double long1er=java.lang.Double.parseDouble(jTextField_Lprimer.getText());
        Double arf1er=java.lang.Double.parseDouble(jTextField_ARFprimer.getText());
        Double long2do=java.lang.Double.parseDouble(jTextField_Lseg.getText());
        Double arf2do=java.lang.Double.parseDouble(jTextField_ARFseg.getText());
        Double stzprot=java.lang.Double.parseDouble(jTextField_STZP.getText());
        int existe;
        try {
            if (tag.equals("insert")) {
                 existe=DBAccess.verificarPkey(id, princ.getSelectedYear(), tablename);
                if(existe!=1)
                {
                    DBAccess.insertCorrFluvial(id,princ.getSelectedYear(),longprinc,arfprinc,long1er,arf1er,long2do,arf2do,stzprot,tablename);
                    jTextField_Lprinc.setText("0.0");
                    jTextField_ARFprinc.setText("0.0");
                    jTextField_Lprimer.setText("0.0");
                    jTextField_ARFprimer.setText("0.0");
                    jTextField_Lseg.setText("0.0");
                    jTextField_ARFseg.setText("0.0");
                    jTextField_STZP.setText("0.0");
                }
                else
                {
                    JOptionPane.showConfirmDialog(this, "Ya existen datos para esta entidad en el a√±o "+princ.getSelectedYear()+".", "Error!!!", JOptionPane.CLOSED_OPTION,JOptionPane.ERROR_MESSAGE);
                }
            } else if (tag.equals("modify")) {
                if(anno==princ.getSelectedYear())
                {
                    DBAccess.updateCorrFluvial(id,princ.getSelectedYear(),anno,longprinc,arfprinc,long1er,arf1er,long2do,arf2do,stzprot,tablename);
                    JOptionPane.showConfirmDialog(this, "Los datos han sido guardados satisfactoriamente.", "Confirmaci√≥n!!!", JOptionPane.CLOSED_OPTION,JOptionPane.INFORMATION_MESSAGE);
                    anno=princ.getSelectedYear();
                }
                else
                {
                    existe=DBAccess.verificarPkey(id, princ.getSelectedYear(), tablename);
                    if(existe!=1)
                    {
                        DBAccess.updateCorrFluvial(id,princ.getSelectedYear(),anno,longprinc,arfprinc,long1er,arf1er,long2do,arf2do,stzprot,tablename);
                        JOptionPane.showConfirmDialog(this, "Los datos han sido guardados satisfactoriamente.", "Confirmaci√≥n!!!", JOptionPane.CLOSED_OPTION,JOptionPane.INFORMATION_MESSAGE);
                        anno=princ.getSelectedYear();
                    }
                    else
                    {
                        JOptionPane.showConfirmDialog(this, "Ya existen datos para esta entidad en el a√±o "+princ.getSelectedYear()+".", "Error!!!", JOptionPane.CLOSED_OPTION,JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            } catch (SQLException ex) {
                Logger.getLogger(Indicador3_2_3.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton_AceptarActionPerformed

